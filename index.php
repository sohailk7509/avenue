  <script>
    document.addEventListener('DOMContentLoaded', function() {
      // Initialize modal
      const blogModal = new bootstrap.Modal(document.getElementById('blogModal'));
      
      // Add event listeners to all Read More buttons
      document.querySelectorAll('.read-more-btn').forEach(button => {
        button.addEventListener('click', function() {
          const blogId = this.getAttribute('data-blog-id');
          loadBlogPost(blogId);
          blogModal.show();
        });
      });
      
      // Function to load blog post content
      function loadBlogPost(blogId) {
        // Reset modal state
        document.getElementById('blogLoader').style.display = 'block';
        document.getElementById('blogContent').style.display = 'none';
        document.getElementById('blogError').style.display = 'none';
        
        // Fetch blog post data
        fetch('get-blog-post.php?id=' + blogId)
          .then(response => {
            if (!response.ok) {
              throw new Error('Network response was not ok');
            }
            return response.json();
          })
          .then(data => {
            if (data.success) {
              // Update modal content with blog data
              document.getElementById('blogModalLabel').innerText = data.title;
              document.getElementById('blogImage').src = data.image;
              document.getElementById('blogImage').alt = data.title;
              document.getElementById('blogAuthor').innerHTML = '<i class="bi bi-person"></i> ' + (data.author_name || 'Admin');
              document.getElementById('blogDate').innerHTML = '<i class="bi bi-calendar"></i> ' + data.formatted_date;
              document.getElementById('blogText').innerHTML = data.content;
              
              // Show content, hide loader
              document.getElementById('blogLoader').style.display = 'none';
              document.getElementById('blogContent').style.display = 'block';
            } else {
              throw new Error(data.message || 'Error loading blog post');
            }
          })
          .catch(error => {
            // Show error message
            document.getElementById('blogLoader').style.display = 'none';
            document.getElementById('blogError').style.display = 'block';
            document.getElementById('blogError').innerText = error.message || 'Error loading blog post. Please try again.';
            console.error('Blog loading error:', error);
          });
      }
    });
  </script>

