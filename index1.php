      // Booking Form Handler
      const bookingForm = document.querySelector('#book-a-table .php-email-form');
      if (bookingForm) {
          bookingForm.addEventListener('submit', function(e) {
              e.preventDefault();
              handleFormSubmit(this, 'forms/book-a-table.php');
          });
      }

      // Newsletter Form Handler
      const newsletterForm = document.querySelector('.footer-newsletter .php-email-form');
      if (newsletterForm) {
          newsletterForm.addEventListener('submit', function(e) {
              e.preventDefault();
              handleFormSubmit(this, 'forms/newsletter.php');
          });
      }

      function handleFormSubmit(form, endpoint) {
          // Show loading
          const loadingDiv = form.querySelector('.loading');
          const errorDiv = form.querySelector('.error-message');
          const successDiv = form.querySelector('.sent-message');
          
          loadingDiv.style.display = 'block';
          errorDiv.style.display = 'none';
          successDiv.style.display = 'none';
          
          const formData = new FormData(form);
          
          fetch(endpoint, {
              method: 'POST',
              body: formData
          })
          .then(response => response.json())
          .then(data => {
              loadingDiv.style.display = 'none';
              
              if (data.success) {
                  successDiv.style.display = 'block';
                  form.reset(); // Clear the form
              } else {
                  errorDiv.textContent = data.message;
                  errorDiv.style.display = 'block';
              }
          })
          .catch(error => {
              loadingDiv.style.display = 'none';
              errorDiv.textContent = 'An error occurred. Please try again.';
              errorDiv.style.display = 'block';
          });
      }
  });
  </script>

